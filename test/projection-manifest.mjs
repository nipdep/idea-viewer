const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

function owlFixturePath(fileName) {
  return path.join(__dirname, 'fixtures/projections/owl', fileName);
}

function owlExpectedPath(fileName) {
  return owlFixturePath(fileName.replace(/\.ttl$/u, '.expected.json'));
}

function rdfFixturePath(fileName) {
  return path.join(__dirname, 'fixtures/projections/rdf', fileName);
}

function rdfExpectedPath(fileName) {
  return rdfFixturePath(fileName.replace(/\.ttl$/u, '.expected.json'));
}

function expandThreeStrategyCases(base) {
  const viewLabel = String(base.view || 'owl').toUpperCase();
  const expectedPath = base.expectedPath ?? (base.view === 'rdf' ? rdfExpectedPath(base.fixtureFile) : owlExpectedPath(base.fixtureFile));
  return [
    {
      ...base,
      id: `${base.idBase}-exact-snapshot`,
      title: `${viewLabel} | ${base.family} | ${base.category} | exact-snapshot`,
      strategy: 'exact-snapshot',
      expectedPath,
    },
    {
      ...base,
      id: `${base.idBase}-structural-semantic`,
      title: `${viewLabel} | ${base.family} | ${base.category} | structural-semantic`,
      strategy: 'structural-semantic',
    },
    {
      ...base,
      id: `${base.idBase}-invariant`,
      title: `${viewLabel} | ${base.family} | ${base.category} | invariant`,
      strategy: 'invariant',
    },
  ];
}

